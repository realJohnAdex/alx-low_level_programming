#include "py.h"

/**
* add - adds two ints
* @a: first integer
* @b: second integer
*
* Return: sum of a & b
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - subtract two ints
* @a: first integer
* @b: second integer
*
* Return: subtract of a & b
*/
int sub(int a, int b)
{
        return (a - b);
}

/**
* mul - multiply two ints
* @a: first integer
* @b: second integer
*
* Return: product of a & b
*/
int mul(int a, int b)
{
        return (a * b);
}

/**
* div - divide two ints
* @a: first integer
* @b: second integer
*
* Return: quotient of a & b
*/
int div(int a, int b)
{
        return (a / b);
}

/**
* mod - adds two ints
* @a: first integer
* @b: second integer
*
* Return: modulus of a & b
*/
int mod(int a, int b)
{
        return (a % b);
}
